package me.horyu.kkutuweb.login

import me.horyu.kkutuweb.setting.OAuthSetting
import org.springframework.beans.factory.annotation.Autowired
import org.springframework.web.bind.annotation.GetMapping
import org.springframework.web.bind.annotation.RequestMapping
import org.springframework.web.bind.annotation.RestController

@RestController
@RequestMapping("/api/login")
class LoginAPI(
    @Autowired private val oAuthSetting: OAuthSetting
) {
    @GetMapping("/vendor")
    fun vendorList(): List<String> {
        return oAuthSetting.getSetting().entries
            .sortedBy { it.value.order }
            .map { it.key.name.toLowerCase() }
    }
}