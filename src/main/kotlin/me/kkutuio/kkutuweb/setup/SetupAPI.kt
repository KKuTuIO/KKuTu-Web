package me.kkutuio.kkutuweb.setup

import me.kkutuio.kkutuweb.result.ActionResult
import org.springframework.beans.factory.annotation.Autowired
import org.springframework.web.bind.annotation.PostMapping
import org.springframework.web.bind.annotation.RequestBody
import org.springframework.web.bind.annotation.RequestMapping
import org.springframework.web.bind.annotation.RestController
import javax.servlet.http.HttpServletRequest
import javax.servlet.http.HttpSession

@RestController
@RequestMapping("/api/setup")
class SetupAPI(
    @Autowired private val setupService: SetupService
) {
    @PostMapping("/nick")
    fun setup(
        @RequestBody nickRequest: NickRequest,
        request: HttpServletRequest,
        session: HttpSession
    ): ActionResult {
        return setupService.setupNick(request, session, nickRequest.nick)
    }
}